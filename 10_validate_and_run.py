def validate(conn):
    """Run all validation checks against the populated database.

    Checks performed:
      1. Row counts:       prints how many rows each table has.
      2. FK integrity:     ensures no orphaned child rows.
      3. Date constraints: ensures end >= start for all temporal pairs.
      4. Uniqueness:       ensures no duplicate values in UNIQUE columns.
      5. Invoice math:     ensures invoice totals = sum of their line items.
    """
    cur = conn.cursor()
    print("\n" + "=" * 60)
    print("VALIDATION")
    print("=" * 60)

    # 1. Row counts
    print("\n--- Row Counts ---")
    total_rows = 0
    for table in ALL_TABLES:
        count = print_table_count(cur, table)
        total_rows += count
    print(f"\n  TOTAL: {total_rows} rows across {len(ALL_TABLES)} tables")

    # 2. FK checks — find child rows that reference non-existent parent rows.
    # Uses NOT EXISTS (correlated subquery) which is efficient on indexed PKs.
    print("\n--- Foreign Key Checks ---")
    fk_errors = 0
    for child_table, child_col, parent_table, parent_col in FK_CHECKS:
        cur.execute(f"""
            SELECT COUNT(*) FROM {child_table} c
            WHERE c.{child_col} IS NOT NULL
              AND NOT EXISTS (
                SELECT 1 FROM {parent_table} p WHERE p.{parent_col} = c.{child_col}
              )
        """)
        orphans = cur.fetchone()[0]
        if orphans > 0:
            print(f"  FAIL: {child_table}.{child_col} -> {parent_table}.{parent_col}: {orphans} orphaned rows")
            fk_errors += orphans
        else:
            print(f"  OK: {child_table}.{child_col} -> {parent_table}.{parent_col}")
    if fk_errors == 0:
        print(f"  All FK checks passed!")

    # 3. Date checks — verify that end >= start for all nullable end columns.
    # Only checks rows where the end column is NOT NULL (NULL means "ongoing").
    print("\n--- Date/Time Constraint Checks ---")
    date_errors = 0
    for table, start_col, end_col in DATE_CHECKS:
        cur.execute(f"""
            SELECT COUNT(*) FROM {table}
            WHERE {end_col} IS NOT NULL AND {end_col} < {start_col}
        """)
        violations = cur.fetchone()[0]
        if violations > 0:
            print(f"  FAIL: {table}.{end_col} < {table}.{start_col}: {violations} violations")
            date_errors += violations
        else:
            print(f"  OK: {table} ({start_col} <= {end_col})")
    if date_errors == 0:
        print(f"  All date checks passed!")

    # 4. Unique checks — detect duplicate values in columns with UNIQUE constraints.
    # GROUP BY + HAVING COUNT > 1 finds any duplicate values.
    print("\n--- Uniqueness Checks ---")
    unique_errors = 0
    for table, col in UNIQUE_CHECKS:
        cur.execute(f"""
            SELECT {col}, COUNT(*) FROM {table}
            WHERE {col} IS NOT NULL
            GROUP BY {col} HAVING COUNT(*) > 1
        """)
        dupes = cur.fetchall()
        if dupes:
            print(f"  FAIL: {table}.{col}: {len(dupes)} duplicate values")
            unique_errors += len(dupes)
        else:
            print(f"  OK: {table}.{col}")
    if unique_errors == 0:
        print(f"  All uniqueness checks passed!")

    # 5. Invoice total vs items — verify that invoice.total_amount equals
    # the sum of its invoice_items.line_amount (within ±0.02 for float rounding).
    print("\n--- Invoice Total vs Items Check ---")
    cur.execute("""
        SELECT i.invoice_id, i.total_amount,
               COALESCE(SUM(ii.line_amount), 0) AS items_total
        FROM invoices i
        LEFT JOIN invoice_items ii ON ii.invoice_id = i.invoice_id
        GROUP BY i.invoice_id, i.total_amount
        HAVING ABS(i.total_amount - COALESCE(SUM(ii.line_amount), 0)) > 0.02
    """)
    mismatches = cur.fetchall()
    if mismatches:
        print(f"  WARNING: {len(mismatches)} invoices have total != sum(items) (tolerance 0.02)")
    else:
        print(f"  OK: All invoice totals match their items")

    print("\n" + "=" * 60)
    total_errors = fk_errors + date_errors + unique_errors
    if total_errors == 0:
        print("ALL VALIDATION CHECKS PASSED!")
    else:
        print(f"TOTAL ERRORS: {total_errors}")
    print("=" * 60)


def main():
    parser = argparse.ArgumentParser(description="Generate synthetic telecom data")
    parser.add_argument("--scale", type=float, default=1.0, help="Scale factor for row counts (0.1 to 10.0)")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for reproducibility")
    args = parser.parse_args()

    scale = max(0.1, min(10.0, args.scale))
    set_seed(args.seed)

    print(f"Scale: {scale}x | Seed: {args.seed}")
    print("=" * 60)

    start_time = time.time()
    conn = get_connection()
    store = DataStore()

    try:
        for script_file in SCRIPTS:
            step_start = time.time()
            label = script_file.replace(".py", "")
            print(f"\n--- Running {label} ---")
            mod = _load_module(script_file)
            mod.generate(conn, scale, store)
            step_elapsed = time.time() - step_start
            print(f"  ({step_elapsed:.1f}s)")

        validate(conn)
        conn.commit()

    except Exception as e:
        conn.rollback()
        print(f"\nERROR: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
    finally:
        conn.close()

    elapsed = time.time() - start_time
    print(f"\nTotal execution time: {elapsed:.1f}s")


if __name__ == "__main__":
    main()
