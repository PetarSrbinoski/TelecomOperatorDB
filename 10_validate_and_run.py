"""
10_validate_and_run.py - Master runner: executes all scripts in order, validates data, reports.

This is the single entry point for the entire data generation pipeline.
It does three things:
  1. Runs scripts 01–09 sequentially, passing a shared DataStore between them
     so that each script can reference IDs generated by earlier scripts.
  2. After all data is inserted, runs a comprehensive validation suite that
     checks FK integrity, date constraints, uniqueness, and invoice math.
  3. Reports row counts, timing, and any validation failures.

Design decisions:
  - Sequential execution (not parallel) because each script depends on IDs
    from earlier scripts.  The FK dependency chain is:
      01_lookups → 02_core_entities → 03_accounts_sims → 04_subscriptions
      → 05_network → 06_network_events → 07_usage_cdrs → 08_billing → 09_crm
  - importlib.util.spec_from_file_location is used instead of importlib.import_module
    because Python can’t import modules whose names start with a digit (01_, 02_, etc.).
  - All inserts share one DB connection, so on failure we rollback everything
    (all-or-nothing) rather than leaving the database in a half-populated state.
  - --scale multiplies all row counts linearly: 0.5x for quick testing,
    5.0x for load testing.  Clamped to [0.1, 10.0] to prevent accidents.
  - --seed resets both Faker and random so the full dataset is reproducible.
  - Validation queries use NOT EXISTS subqueries (not JOINs) to efficiently
    detect orphaned FK references even on large tables.

Usage:
    python 10_validate_and_run.py [--scale 1.0] [--seed 42]
"""

